	>
		{@render asideSlot()}
	</aside>
	<div class="order-2 grow overflow-x-hidden">
		<div
			class="relative border-r border-kui-light-gray-200 dark:border-kui-dark-gray-400 flex h-full w-full flex-col"
		>
